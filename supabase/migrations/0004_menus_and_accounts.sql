-- ─── 2) booth_accounts — 50 계정 (login_id = booth_no, pw = '123456') ──────
-- bcrypt cost 10 (bcryptjs 호환)

INSERT INTO booth_accounts (booth_id, login_id, password_hash)
SELECT b.id, b.booth_no, crypt('123456', gen_salt('bf', 10))
  FROM food_booths b
  JOIN festivals f ON b.festival_id = f.id AND f.slug = 'food'
 WHERE b.booth_no IS NOT NULL
   AND NOT EXISTS (
     SELECT 1 FROM booth_accounts ba WHERE ba.login_id = b.booth_no
   );


